package test;

/**
 * математические операции
 */
public class Test {

    /**
     * сумма
     * @param a первое слог
     * @param b второе слог
     * @return возврат результата
     */
    public int sum( int a, int b){
        return a + b;
    }

    /**
     * умножение
     * @param a первое слог
     * @param b второе слог
     * @return возврат результата
     */
    public int mul( int a, int b){
        return a * b;
    }

    /**
     * деление
     * @param a первое слог
     * @param b второе слог
     * @return возврат результата
     */
    public int div( int a, int b) {
        return a / b;
    }

    /**
     * вычетание
     * @param a первое слог
     * @param b второе слог
     * @return возврат результата
     */
    public int sub(int a, int b){
        return a - b;
    }


}
